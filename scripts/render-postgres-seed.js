const crypto = require('node:crypto');
const fs = require('node:fs/promises');
const path = require('node:path');

const { PACKAGE_CONFIG } = require('../api/_lib/config');

function isUuid(value) {
  return /^[0-9a-f]{8}-[0-9a-f]{4}-[1-5][0-9a-f]{3}-[89ab][0-9a-f]{3}-[0-9a-f]{12}$/i.test(String(value || ''));
}

function stableUuid(seed) {
  const hex = crypto.createHash('sha256').update(String(seed || '')).digest('hex').slice(0, 32);
  return [
    hex.slice(0, 8),
    hex.slice(8, 12),
    `4${hex.slice(13, 16)}`,
    `a${hex.slice(17, 20)}`,
    hex.slice(20, 32)
  ].join('-');
}

function ensureUuid(value, fallbackSeed) {
  if (isUuid(value)) {
    return String(value);
  }

  return stableUuid(fallbackSeed || value || crypto.randomUUID());
}

function sqlLiteral(value) {
  if (value === null || value === undefined) {
    return 'NULL';
  }

  return `'${String(value).replace(/'/g, "''")}'`;
}

function sqlJson(value) {
  return `${sqlLiteral(JSON.stringify(value || {}))}::jsonb`;
}

function sqlTimestamp(value) {
  return value ? sqlLiteral(value) : 'NULL';
}

function sqlDate(value) {
  return value ? sqlLiteral(value) : 'NULL';
}

function hashToken(token) {
  return crypto.createHash('sha256').update(String(token || ''), 'utf8').digest('hex');
}

function normalizeEmail(value) {
  return String(value || '').trim().toLowerCase();
}

function customerKey(caseRecord) {
  return `${normalizeEmail(caseRecord.clientEmail)}::${String(caseRecord.clientName || '').trim().toLowerCase()}`;
}

function partnerKey(caseRecord) {
  return [
    String(caseRecord.referralPartnerType || 'direct').trim().toLowerCase(),
    String(caseRecord.referralPartnerName || '').trim().toLowerCase(),
    String(caseRecord.referralPartnerEmail || '').trim().toLowerCase()
  ].join('::');
}

function buildUserLookup(users) {
  const byIdentity = new Map();

  for (const user of users) {
    const userId = ensureUuid(user.id, `user:${user.username || user.name || ''}`);
    byIdentity.set(String(user.username || '').trim().toLowerCase(), userId);
    byIdentity.set(String(user.name || '').trim().toLowerCase(), userId);
  }

  return byIdentity;
}

async function main() {
  const snapshotPath = process.argv[2];

  if (!snapshotPath) {
    console.error('Usage: npm run ops:render-seed -- exports/<snapshot-file>.json');
    process.exit(1);
  }

  const resolvedPath = path.resolve(process.cwd(), snapshotPath);
  const raw = await fs.readFile(resolvedPath, 'utf8');
  const snapshot = JSON.parse(raw);
  const users = Array.isArray(snapshot.users) ? snapshot.users : [];
  const cases = Array.isArray(snapshot.rawCases) ? snapshot.rawCases : [];
  const analyticsEvents = Array.isArray(snapshot.analyticsEvents) ? snapshot.analyticsEvents : [];
  const userLookup = buildUserLookup(users);
  const existingCaseIds = new Set(cases.map((entry) => entry.id));

  const customerIds = new Map();
  const partnerIds = new Map();

  for (const caseRecord of cases) {
    const cKey = customerKey(caseRecord);
    if (!customerIds.has(cKey)) {
      customerIds.set(cKey, crypto.randomUUID());
    }

    if (caseRecord.referralPartnerName) {
      const pKey = partnerKey(caseRecord);
      if (!partnerIds.has(pKey)) {
        partnerIds.set(pKey, crypto.randomUUID());
      }
    }
  }

  const statements = [];
  statements.push('-- Generated by scripts/render-postgres-seed.js');
  statements.push(`-- Snapshot: ${resolvedPath}`);
  statements.push(`-- Generated at: ${new Date().toISOString()}`);
  statements.push('BEGIN;');
  statements.push('');

  for (const user of users) {
    const userId = ensureUuid(user.id, `user:${user.username || user.name || ''}`);
    statements.push(
      `INSERT INTO app_users (id, username, display_name, role, status, password_hash, created_at, updated_at)
VALUES (${sqlLiteral(userId)}, ${sqlLiteral(user.username)}, ${sqlLiteral(user.name || user.username)}, ${sqlLiteral(user.role || 'founder_admin')}, ${sqlLiteral(user.status || 'active')}, ${sqlLiteral(user.passwordHash)}, ${sqlTimestamp(user.createdAt)}, ${sqlTimestamp(user.updatedAt)})
ON CONFLICT (id) DO UPDATE SET
  username = EXCLUDED.username,
  display_name = EXCLUDED.display_name,
  role = EXCLUDED.role,
  status = EXCLUDED.status,
  password_hash = EXCLUDED.password_hash,
  updated_at = EXCLUDED.updated_at;`
    );
  }

  statements.push('');

  for (const caseRecord of cases) {
    const cKey = customerKey(caseRecord);
    const customerId = customerIds.get(cKey);

    statements.push(
      `INSERT INTO customers (id, full_name, email, phone, relationship_to_deceased, created_at, updated_at)
VALUES (${sqlLiteral(customerId)}, ${sqlLiteral(caseRecord.clientName)}, ${sqlLiteral(caseRecord.clientEmail)}, ${sqlLiteral(caseRecord.clientPhone || '')}, ${sqlLiteral(caseRecord.relationshipToDeceased || '')}, ${sqlTimestamp(caseRecord.createdAt)}, ${sqlTimestamp(caseRecord.updatedAt)})
ON CONFLICT (id) DO UPDATE SET
  full_name = EXCLUDED.full_name,
  email = EXCLUDED.email,
  phone = EXCLUDED.phone,
  relationship_to_deceased = EXCLUDED.relationship_to_deceased,
  updated_at = EXCLUDED.updated_at;`
    );
  }

  statements.push('');

  for (const caseRecord of cases.filter((entry) => entry.referralPartnerName)) {
    const pKey = partnerKey(caseRecord);
    const partnerId = partnerIds.get(pKey);
    statements.push(
      `INSERT INTO partner_accounts (id, partner_type, business_name, primary_contact_name, email, phone, referral_fee_gbp, status, notes, created_at, updated_at)
VALUES (${sqlLiteral(partnerId)}, ${sqlLiteral(caseRecord.referralPartnerType || 'funeral_director')}, ${sqlLiteral(caseRecord.referralPartnerName)}, ${sqlLiteral(caseRecord.referralPartnerName)}, ${sqlLiteral(caseRecord.referralPartnerEmail || '')}, ${sqlLiteral(caseRecord.referralPartnerPhone || '')}, 50.00, 'active', ${sqlLiteral(caseRecord.referralNotes || '')}, ${sqlTimestamp(caseRecord.createdAt)}, ${sqlTimestamp(caseRecord.updatedAt)})
ON CONFLICT (id) DO UPDATE SET
  partner_type = EXCLUDED.partner_type,
  business_name = EXCLUDED.business_name,
  primary_contact_name = EXCLUDED.primary_contact_name,
  email = EXCLUDED.email,
  phone = EXCLUDED.phone,
  notes = EXCLUDED.notes,
  updated_at = EXCLUDED.updated_at;`
    );
  }

  statements.push('');

  for (const caseRecord of cases) {
    const cKey = customerKey(caseRecord);
    const customerId = customerIds.get(cKey);
    const pKey = caseRecord.referralPartnerName ? partnerKey(caseRecord) : null;
    const partnerId = pKey ? partnerIds.get(pKey) : null;
    const packageConfig = PACKAGE_CONFIG[caseRecord.selectedPackage] || PACKAGE_CONFIG.standard;
    const assignedUserId = userLookup.get(String(caseRecord.assignedTo || '').trim().toLowerCase()) || null;
    const archivedByUserId = userLookup.get(String(caseRecord.archivedBy || '').trim().toLowerCase()) || null;

    statements.push(
      `INSERT INTO cases (
  id,
  reference,
  public_token_hash,
  public_token_hint,
  customer_id,
  partner_account_id,
  intake_source,
  referral_source,
  deceased_name,
  preferred_outcome,
  case_details,
  relationship_to_deceased,
  urgency,
  selected_package,
  package_label,
  package_price_gbp,
  package_target_days,
  status,
  payment_status,
  priority,
  assigned_to_user_id,
  assigned_to_name_snapshot,
  due_date,
  next_follow_up_at,
  operator_lane,
  blocker_reason,
  authority_basis,
  document_notes,
  internal_notes,
  archived_at,
  archived_by_user_id,
  archived_by_name_snapshot,
  archive_reason,
  last_client_update_at,
  last_operator_action_at,
  created_at,
  updated_at,
  legacy_payload
) VALUES (
  ${sqlLiteral(caseRecord.id)},
  ${sqlLiteral(caseRecord.reference)},
  ${sqlLiteral(hashToken(caseRecord.publicToken))},
  ${sqlLiteral(String(caseRecord.publicToken || '').slice(-6))},
  ${sqlLiteral(customerId)},
  ${partnerId ? sqlLiteral(partnerId) : 'NULL'},
  ${sqlLiteral(caseRecord.intakeSource || 'website')},
  ${sqlLiteral(caseRecord.referralSource || '')},
  ${sqlLiteral(caseRecord.deceasedName)},
  ${sqlLiteral(caseRecord.preferredOutcome || 'not_sure')},
  ${sqlLiteral(caseRecord.caseDetails || '')},
  ${sqlLiteral(caseRecord.relationshipToDeceased || '')},
  ${sqlLiteral(caseRecord.urgency || 'standard')},
  ${sqlLiteral(caseRecord.selectedPackage || 'standard')},
  ${sqlLiteral(caseRecord.packageLabel || packageConfig.label)},
  ${packageConfig.price.toFixed(2)},
  ${Number(packageConfig.targetDays || 10)},
  ${sqlLiteral(caseRecord.status || 'awaiting_payment')},
  ${sqlLiteral(caseRecord.paymentStatus || 'pending')},
  ${sqlLiteral(caseRecord.priority || 'standard')},
  ${assignedUserId ? sqlLiteral(assignedUserId) : 'NULL'},
  ${sqlLiteral(caseRecord.assignedTo || '')},
  ${sqlDate(caseRecord.dueDate || '')},
  ${sqlDate(caseRecord.nextFollowUpAt || '')},
  ${sqlLiteral(caseRecord.operatorLane || '')},
  ${sqlLiteral(caseRecord.blockerReason || '')},
  ${sqlLiteral(caseRecord.authorityBasis || '')},
  ${sqlLiteral(caseRecord.documentNotes || '')},
  ${sqlLiteral(caseRecord.internalNotes || '')},
  ${sqlTimestamp(caseRecord.archivedAt || '')},
  ${archivedByUserId ? sqlLiteral(archivedByUserId) : 'NULL'},
  ${sqlLiteral(caseRecord.archivedBy || '')},
  ${sqlLiteral(caseRecord.archiveReason || '')},
  ${sqlTimestamp(caseRecord.lastClientUpdateAt || caseRecord.createdAt)},
  ${sqlTimestamp(caseRecord.lastOperatorActionAt || '')},
  ${sqlTimestamp(caseRecord.createdAt)},
  ${sqlTimestamp(caseRecord.updatedAt)},
  ${sqlJson(caseRecord)}
)
ON CONFLICT (id) DO UPDATE SET
  customer_id = EXCLUDED.customer_id,
  partner_account_id = EXCLUDED.partner_account_id,
  intake_source = EXCLUDED.intake_source,
  referral_source = EXCLUDED.referral_source,
  deceased_name = EXCLUDED.deceased_name,
  preferred_outcome = EXCLUDED.preferred_outcome,
  case_details = EXCLUDED.case_details,
  relationship_to_deceased = EXCLUDED.relationship_to_deceased,
  urgency = EXCLUDED.urgency,
  selected_package = EXCLUDED.selected_package,
  package_label = EXCLUDED.package_label,
  package_price_gbp = EXCLUDED.package_price_gbp,
  package_target_days = EXCLUDED.package_target_days,
  status = EXCLUDED.status,
  payment_status = EXCLUDED.payment_status,
  priority = EXCLUDED.priority,
  assigned_to_user_id = EXCLUDED.assigned_to_user_id,
  assigned_to_name_snapshot = EXCLUDED.assigned_to_name_snapshot,
  due_date = EXCLUDED.due_date,
  next_follow_up_at = EXCLUDED.next_follow_up_at,
  operator_lane = EXCLUDED.operator_lane,
  blocker_reason = EXCLUDED.blocker_reason,
  authority_basis = EXCLUDED.authority_basis,
  document_notes = EXCLUDED.document_notes,
  internal_notes = EXCLUDED.internal_notes,
  archived_at = EXCLUDED.archived_at,
  archived_by_user_id = EXCLUDED.archived_by_user_id,
  archived_by_name_snapshot = EXCLUDED.archived_by_name_snapshot,
  archive_reason = EXCLUDED.archive_reason,
  last_client_update_at = EXCLUDED.last_client_update_at,
  last_operator_action_at = EXCLUDED.last_operator_action_at,
  updated_at = EXCLUDED.updated_at,
  legacy_payload = EXCLUDED.legacy_payload;`
    );

    for (const [index, task] of (caseRecord.platformTasks || []).entries()) {
      statements.push(
        `INSERT INTO platform_tasks (id, case_id, external_key, platform_name, profile_or_handle, status, outcome_requested, evidence_needed, notes, submission_reference, submitted_at, resolved_at, last_updated_at, sort_order, created_at, updated_at)
VALUES (${sqlLiteral(task.id || crypto.randomUUID())}, ${sqlLiteral(caseRecord.id)}, ${sqlLiteral(task.id || `platform-${index + 1}`)}, ${sqlLiteral(task.name || `Platform ${index + 1}`)}, ${sqlLiteral(task.profileOrHandle || '')}, ${sqlLiteral(task.status || 'not_started')}, ${sqlLiteral(task.outcomeRequested || '')}, ${sqlLiteral(task.evidenceNeeded || '')}, ${sqlLiteral(task.notes || '')}, ${sqlLiteral(task.submissionReference || '')}, ${sqlTimestamp(task.submittedAt || '')}, ${sqlTimestamp(task.resolvedAt || '')}, ${sqlTimestamp(task.lastUpdatedAt || caseRecord.updatedAt)}, ${index}, ${sqlTimestamp(caseRecord.createdAt)}, ${sqlTimestamp(task.lastUpdatedAt || caseRecord.updatedAt)})
ON CONFLICT (id) DO UPDATE SET
  profile_or_handle = EXCLUDED.profile_or_handle,
  status = EXCLUDED.status,
  outcome_requested = EXCLUDED.outcome_requested,
  evidence_needed = EXCLUDED.evidence_needed,
  notes = EXCLUDED.notes,
  submission_reference = EXCLUDED.submission_reference,
  submitted_at = EXCLUDED.submitted_at,
  resolved_at = EXCLUDED.resolved_at,
  last_updated_at = EXCLUDED.last_updated_at,
  sort_order = EXCLUDED.sort_order,
  updated_at = EXCLUDED.updated_at;`
      );
    }

    for (const document of (caseRecord.documents || [])) {
      statements.push(
        `INSERT INTO case_documents (id, case_id, file_name, document_type, content_type, size_bytes, storage_provider, storage_path, uploaded_by_actor, uploaded_at, metadata)
VALUES (${sqlLiteral(document.id)}, ${sqlLiteral(caseRecord.id)}, ${sqlLiteral(document.fileName || '')}, ${sqlLiteral(document.documentType || 'supporting_document')}, ${sqlLiteral(document.contentType || 'application/octet-stream')}, ${Number(document.size || 0)}, 'blob', ${sqlLiteral(document.storagePath || '')}, 'public', ${sqlTimestamp(document.uploadedAt || caseRecord.updatedAt)}, ${sqlJson(document)})
ON CONFLICT (id) DO UPDATE SET
  file_name = EXCLUDED.file_name,
  document_type = EXCLUDED.document_type,
  content_type = EXCLUDED.content_type,
  size_bytes = EXCLUDED.size_bytes,
  storage_path = EXCLUDED.storage_path,
  uploaded_at = EXCLUDED.uploaded_at,
  metadata = EXCLUDED.metadata;`
      );
    }

    for (const reminder of (caseRecord.reminders || [])) {
      statements.push(
        `INSERT INTO case_reminders (id, case_id, title, status, severity, assigned_to, owner_lane, due_date, escalate_at, notes, completed_at, created_at, updated_at)
VALUES (${sqlLiteral(reminder.id || crypto.randomUUID())}, ${sqlLiteral(caseRecord.id)}, ${sqlLiteral(reminder.title || 'Reminder')}, ${sqlLiteral(reminder.status || 'open')}, ${sqlLiteral(reminder.severity || 'normal')}, ${sqlLiteral(reminder.assignedTo || '')}, ${sqlLiteral(reminder.ownerLane || '')}, ${sqlDate(reminder.dueDate || '')}, ${sqlDate(reminder.escalateAt || '')}, ${sqlLiteral(reminder.notes || '')}, ${sqlTimestamp(reminder.completedAt || '')}, ${sqlTimestamp(reminder.createdAt || caseRecord.createdAt)}, ${sqlTimestamp(reminder.updatedAt || caseRecord.updatedAt)})
ON CONFLICT (id) DO UPDATE SET
  title = EXCLUDED.title,
  status = EXCLUDED.status,
  severity = EXCLUDED.severity,
  assigned_to = EXCLUDED.assigned_to,
  owner_lane = EXCLUDED.owner_lane,
  due_date = EXCLUDED.due_date,
  escalate_at = EXCLUDED.escalate_at,
  notes = EXCLUDED.notes,
  completed_at = EXCLUDED.completed_at,
  updated_at = EXCLUDED.updated_at;`
      );
    }

    for (const activity of (caseRecord.activity || [])) {
      statements.push(
        `INSERT INTO case_activity (id, case_id, actor_type, actor_label, event_type, metadata, created_at)
VALUES (${sqlLiteral(activity.id || crypto.randomUUID())}, ${sqlLiteral(caseRecord.id)}, ${sqlLiteral(activity.actor || 'system')}, ${sqlLiteral(activity.actor || 'system')}, ${sqlLiteral(activity.eventType || 'unknown_event')}, ${sqlJson(activity.metadata || {})}, ${sqlTimestamp(activity.createdAt || caseRecord.createdAt)})
ON CONFLICT (id) DO NOTHING;`
      );
    }

    statements.push('');
  }

  for (const event of analyticsEvents) {
    const safeCaseId = event.caseId && existingCaseIds.has(event.caseId) ? event.caseId : null;
    statements.push(
      `INSERT INTO analytics_events (id, session_id, case_id, event_type, path, label, page_title, referrer, metadata, created_at)
VALUES (${sqlLiteral(event.id)}, ${sqlLiteral(event.sessionId || '')}, ${safeCaseId ? sqlLiteral(safeCaseId) : 'NULL'}, ${sqlLiteral(event.eventType || '')}, ${sqlLiteral(event.path || '')}, ${sqlLiteral(event.label || '')}, ${sqlLiteral(event.pageTitle || '')}, ${sqlLiteral(event.referrer || '')}, ${sqlJson(event.metadata || {})}, ${sqlTimestamp(event.createdAt)})
ON CONFLICT (id) DO NOTHING;`
    );
  }

  statements.push('');
  statements.push('COMMIT;');

  const outputDir = path.join(process.cwd(), 'exports');
  const outputPath = path.join(outputDir, `postgres-seed-${new Date().toISOString().replace(/[:.]/g, '-')}.sql`);
  await fs.mkdir(outputDir, { recursive: true });
  await fs.writeFile(outputPath, statements.join('\n'));

  process.stdout.write(JSON.stringify({
    ok: true,
    outputPath,
    cases: cases.length,
    users: users.length,
    analyticsEvents: analyticsEvents.length
  }, null, 2) + '\n');
}

main().catch((error) => {
  console.error('postgres_seed_render_failed');
  console.error(error);
  process.exitCode = 1;
});
